package com.amazonaws.youruserpools;

class Category3 {
    private int id;
    private String type_name;

    public Category3(int id, String type_name) {
        this.id = id;
        this.type_name = type_name;
    }

    public int getid() {
        return this.id;
    }

    public String getName3() {
        return this.type_name;
    }
}